let ilerlemeDurumu = { "azerbaycan dili": 0, "fizik": 0, "kimya": 0, "biyoloji": 0 };
let cevaplarHafizasi = { "azerbaycan dili": {}, "fizik": {}, "kimya": {}, "biyoloji": {} };
let aktifKategori = "azerbaycan dili";

const soruMetniElement = document.getElementById("soru-metni");
const seceneklerAlaniElement = document.getElementById("secenekler-alani");
const soruSayaciElement = document.getElementById("soru-sayaci");
const aktifDersAdiElement = document.getElementById("aktif-ders-adi");
const sonrakiBtn = document.getElementById("sonraki-btn");
const soruAraInput = document.getElementById("soru-ara-input");

function sekmeDegistir(yeniKategori) {
    aktifKategori = yeniKategori;
    const butonlar = document.querySelectorAll(".menu-btn");
    butonlar.forEach(btn => {
        const altNav = btn.nextElementSibling;
        if (btn.getAttribute("onclick").includes(yeniKategori)) {
            btn.classList.add("active");
            if (altNav) altNav.style.display = "grid";
        } else {
            btn.classList.remove("active");
            if (altNav) altNav.style.display = "none";
        }
    });
    aktifDersAdiElement.innerText = yeniKategori;
    soruyuYukle();
}

function solNavigasyonGuncelle() {
    const idMap = { "azerbaycan dili": "nav-azerbaycan-dili", "fizik": "nav-fizik", "kimya": "nav-kimya", "biyoloji": "nav-biyoloji" };
    const hedefId = idMap[aktifKategori];
    const navAlani = document.getElementById(hedefId);
    if (!navAlani) return;

    navAlani.innerHTML = "";
    const soruListesi = tumSorular[aktifKategori];

    soruListesi.forEach((_, index) => {
        const numButon = document.createElement("button");
        numButon.innerText = index + 1;
        numButon.classList.add("nav-num-btn");

        if (index === ilerlemeDurumu[aktifKategori]) numButon.classList.add("current");

        const gecmisDurum = cevaplarHafizasi[aktifKategori][index];
        if (gecmisDurum === "correct") numButon.classList.add("nav-correct");
        if (gecmisDurum === "wrong") numButon.classList.add("nav-wrong");

        numButon.addEventListener("click", () => {
            ilerlemeDurumu[aktifKategori] = index;
            soruyuYukle();
        });
        navAlani.appendChild(numButon);
    });
}

function soruyuYukle() {
    sonrakiBtn.style.display = "none";
    seceneklerAlaniElement.innerHTML = "";
    solNavigasyonGuncelle();

    const soruListesi = tumSorular[aktifKategori];
    const guncelSoruIndex = ilerlemeDurumu[aktifKategori];

    if (!soruListesi || soruListesi.length === 0) {
        soruSayaciElement.innerText = "Soru: 0 / 0";
        soruMetniElement.innerText = "Bu bölüme henüz soru yüklenmedi.";
        return;
    }

    if (guncelSoruIndex >= soruListesi.length) {
        soruSayaciElement.innerText = `Soru: ${soruListesi.length} / ${soruListesi.length}`;
        soruMetniElement.innerText = "Tebrikler! Bu bölümdeki tüm soruları tamamladınız.";
        return;
    }

    const aktifSoru = soruListesi[guncelSoruIndex];
    soruSayaciElement.innerText = `Soru: ${guncelSoruIndex + 1} / ${soruListesi.length}`;
    soruMetniElement.innerText = aktifSoru.soru;

    const dahaOncekiCevap = cevaplarHafizasi[aktifKategori][guncelSoruIndex];

    aktifSoru.secenekler.forEach(secenek => {
        const buton = document.createElement("button");
        buton.innerText = secenek;
        buton.classList.add("option-btn");
        
        if (dahaOncekiCevap) {
            buton.disabled = true;
            if (secenek === aktifSoru.cevap) buton.classList.add("correct");
        } else {
            buton.addEventListener("click", () => cevapKontrolEt(buton, secenek, aktifSoru.cevap, guncelSoruIndex));
        }
        seceneklerAlaniElement.appendChild(buton);
    });

    if (dahaOncekiCevap) sonrakiBtn.style.display = "block";
}

function cevapKontrolEt(secilenButon, secilenCevap, dogruCevap, soruIndex) {
    const tumButonlar = seceneklerAlaniElement.querySelectorAll(".option-btn");
    tumButonlar.forEach(btn => btn.disabled = true);

    if (secilenCevap === dogruCevap) {
        secilenButon.classList.add("correct");
        cevaplarHafizasi[aktifKategori][soruIndex] = "correct";
        solNavigasyonGuncelle();
        
        setTimeout(() => {
            ilerlemeDurumu[aktifKategori]++;
            soruyuYukle();
        }, 500);
    } else {
        secilenButon.classList.add("wrong");
        cevaplarHafizasi[aktifKategori][soruIndex] = "wrong";
        solNavigasyonGuncelle();
        
        tumButonlar.forEach(btn => {
            if (btn.innerText === dogruCevap) btn.classList.add("correct");
        });
        sonrakiBtn.style.display = "block";
    }
}

sonrakiBtn.addEventListener("click", () => {
    ilerlemeDurumu[aktifKategori]++;
    soruyuYukle();
});

soruAraInput.addEventListener("keydown", (event) => {
    if (event.key === "Enter") {
        const girilenDeger = parseInt(soruAraInput.value);
        const toplamSoruSayisi = tumSorular[aktifKategori].length;

        if (!isNaN(girilenDeger) && girilenDeger >= 1 && girilenDeger <= toplamSoruSayisi) {
            ilerlemeDurumu[aktifKategori] = girilenDeger - 1;
            soruyuYukle();
            soruAraInput.value = "";
            soruAraInput.blur();
        } else {
            alert(`Lütfen 1 ile ${toplamSoruSayisi} arasında geçerli bir soru numarası girin!`);
        }
    }
});

solNavigasyonGuncelle();
soruyuYukle();